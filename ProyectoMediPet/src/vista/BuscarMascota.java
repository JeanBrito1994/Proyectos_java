    private void btn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EliminarActionPerformed
       ControladorMascota ctrlm = new ControladorMascota();
        try{
            if(tbl_mascota.getSelectedRow()==-1){
                JOptionPane.showMessageDialog(rootPane, "Seleccione una opción");
            }else{
                
                
            int indice = tbl_mascota.getSelectedRow();
            String codigo = tbl_mascota.getValueAt(indice, 0).toString();
            
            
            ctrlm.eliminaMascota(codigo);
            
            JOptionPane.showMessageDialog(rootPane, "MASCOTA SE ELIMINÓ CORRECTAMENTE");
            btn_buscarActionPerformed(evt);
            }
        }
        catch(Exception err){
            JOptionPane.showMessageDialog(rootPane, "ERROR " + err.getMessage());
        }
    }//GEN-LAST:event_btn_EliminarActionPerformed

