    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        ArrayList<Mascota> listaMascotas = new ArrayList<>();
        ControladorMascota ctrm = new ControladorMascota();
        try{
           
            String nombre = txt_buscar.getText();
            String tipo = cmb_mascota_tipo.getSelectedItem().toString();

            if(rb_buscar_nombre.isSelected()==true){
            listaMascotas = ctrm.listarMascota(nombre);
            }else if(rb_buscar_tipo.isSelected()==true){
                listaMascotas = ctrm.listarMascotaTipo(tipo);
            }

            DefaultTableModel tablaModelo = (DefaultTableModel)tbl_mascota.getModel();
            tablaModelo.setRowCount(0);
            
            for(Mascota x: listaMascotas){
               Object[] fila = {
                x.getId(),
                x.getNombre(),
                x.getEdad(),
                x.getTipo(),
                x.isPosee_dueno()};
                
                tablaModelo.addRow(fila);
            }            
    }
            catch(Exception err){
            System.out.println("Error: " + err.getMessage());
        }             
    }//GEN-LAST:event_btn_buscarActionPerformed

