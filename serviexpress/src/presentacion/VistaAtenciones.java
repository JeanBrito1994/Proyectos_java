    private void btn_cancelar_reservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cancelar_reservaActionPerformed
        Logica_documento lg_documento = new Logica_documento();
        try{
            if(tbl_atencion.getSelectedRow()==-1){
                JOptionPane.showMessageDialog(rootPane, "Seleccione una opción");
            }else{
            int indice = tbl_atencion.getSelectedRow();
            int codigo = (int) tbl_atencion.getValueAt(indice, 0);
            lg_documento.anularReserva(codigo);
            JOptionPane.showMessageDialog(rootPane, "Cancelada Correctamente!!");
            actualizarTabla();
            }
            
        }
        catch(Exception err){
            JOptionPane.showMessageDialog(rootPane, "Error: " + err.getMessage());
        }
    }//GEN-LAST:event_btn_cancelar_reservaActionPerformed

