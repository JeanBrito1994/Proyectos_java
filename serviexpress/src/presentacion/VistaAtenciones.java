    private void btn_verActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_verActionPerformed
        if(tbl_atencion.getSelectedRow()==-1){
                JOptionPane.showMessageDialog(rootPane, "Seleccione una opción");
            }else{
        
        int indice = tbl_atencion.getSelectedRow();
        int cod_atencion = (int) tbl_atencion.getValueAt(indice, 0);
        cod = cod_atencion;
        
        VistaAtencionesDetalle vista_detalle = new VistaAtencionesDetalle();
        vista_detalle.setVisible(true);
        dispose();
        }
        
        
    }//GEN-LAST:event_btn_verActionPerformed

    public void actualizarTabla(){
        ArrayList<Atencion> listaAtencion = new ArrayList<>();
        Logica_documento lg_doc = new Logica_documento();
        
        
        
        try{
            
            String estado = cmb_status.getSelectedItem().toString();
            
            listaAtencion = lg_doc.listarAtenciones(estado);
            DefaultTableModel tablaModelo = (DefaultTableModel)tbl_atencion.getModel();
            tablaModelo.setRowCount(0);
            
            for(Atencion x: listaAtencion){
               Object[] fila = {
                x.getId(),
                x.getFecha_reserva(),
                x.getStatus()};
               
                tablaModelo.addRow(fila);
                
            }
     
        }
        catch(Exception err){
            
            System.out.println("Error: " + err.getMessage());
        }
    }
    
    
    
