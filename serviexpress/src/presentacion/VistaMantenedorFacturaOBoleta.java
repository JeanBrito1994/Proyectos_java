    public void actualizarTabla(){
        ArrayList<Documento_pago> lista_pago = new ArrayList<>();
        Logica_documento lg_doc = new Logica_documento();
        int codigo_a_buscar = Integer.parseInt(txt_codigo.getText());
        try{
            
            lista_pago = lg_doc.listarPagos(codigo_a_buscar);
            
            DefaultTableModel tablaModelo = (DefaultTableModel)tbl_pagos.getModel();
            tablaModelo.setRowCount(0);
            
            for(Documento_pago x: lista_pago){
               Object[] fila = {
                x.getId(),
                x.getFecha_pago(),
                x.getTotal(),
                x.getTipo_documento(),
                x.getEstado()};
               
                tablaModelo.addRow(fila);
                
            }
     
        }
        catch(Exception err){
            
            System.out.println("Error: " + err.getMessage());
        }
    }
