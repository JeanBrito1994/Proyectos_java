    private void btn_anularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_anularActionPerformed
        Logica_documento lg_documento = new Logica_documento();
        try{
            if(tbl_pagos.getSelectedRow()==-1){
                JOptionPane.showMessageDialog(rootPane, "Seleccione una opción");
            }else{
            int indice = tbl_pagos.getSelectedRow();
            int codigo = (int) tbl_pagos.getValueAt(indice, 0);
            lg_documento.anularDocumento(codigo);
            JOptionPane.showMessageDialog(rootPane, "Anulado Correctamente!!");
            actualizarTabla();
            }
            

           
        }
        catch(Exception err){
            JOptionPane.showMessageDialog(rootPane, "Error: " + err.getMessage());
        }
    }//GEN-LAST:event_btn_anularActionPerformed

