    private void btn_generar_ordenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_generar_ordenActionPerformed
        try{
            int numeroAleatorio = (int) (Math.random()*25+1);
            Document documento = new Document();
            DefaultTableModel tablaModelo = (DefaultTableModel)tbl_carrito.getModel();
            String ruta = System.getProperty("user.home");
            PdfWriter.getInstance(documento, new FileOutputStream(ruta + "/Desktop/Orden de compra"+ numeroAleatorio+".pdf")).setInitialLeading(30);
            
            documento.open();
            
            Image img = Image.getInstance("src/img/logo_adm.png");
            
            Paragraph fecha = new Paragraph();
            fecha.add(Chunk.NEWLINE);
            Date date = new Date();
            fecha.add("Fecha: " + new SimpleDateFormat("dd-MM-yyyy").format(date)+ "\n\n");
            
            PdfPTable Encabezado = new PdfPTable(4);
            Encabezado.setWidthPercentage(100);
            Encabezado.getDefaultCell().setBorder(0);
            float[] ColumaEncabezado = new float[]{20f, 30f, 70f, 40f};
            Encabezado.setWidths(ColumaEncabezado);
            Encabezado.setHorizontalAlignment(Element.ALIGN_LEFT);
            
            Encabezado.addCell(img);
            Encabezado.addCell("");
            Encabezado.addCell("");
            Encabezado.addCell(fecha);
            
            documento.add(Encabezado);
            
            Paragraph Title = new Paragraph();
            Title.setAlignment(Element.ALIGN_CENTER) ; 
            Font negrita = new Font(Font.FontFamily.TIMES_ROMAN, 25, Font.BOLD);
            Title.setFont(negrita);


            Title.add(Chunk.NEWLINE);
            Title.add("Orden de Compra"+"\n\n");
            documento.add(Title);
            
            
            Paragraph Cuerpo = new Paragraph();
            Cuerpo.setAlignment(Element.ALIGN_LEFT); 
            Font negrocuerpo = new Font(Font.FontFamily.TIMES_ROMAN, 14, Font.BOLD);
            Cuerpo.setFont(negrocuerpo);
            
            Cuerpo.add(Chunk.NEWLINE);
            Cuerpo.add("Nombre de la empresa:  " + txt_proveedor.getText()+ "\n\n");
            Cuerpo.add("Ciudad:  Santiago" + "\n\n");
            Cuerpo.add("Total: $ "+ txt_total.getText()+ "\n\n");
            Cuerpo.add(Chunk.NEWLINE);
            documento.add(Cuerpo);
                
            
            PdfPTable tabla = new PdfPTable(3);
            
            tabla.addCell("Codigo");
            tabla.addCell("Nombre de producto");
            tabla.addCell("Precio");
            
            try{
                
                for (int indice = 0; indice < tablaModelo.getRowCount(); indice++){
                String codigo = tbl_carrito.getValueAt(indice, 0).toString();
                String nombre = tbl_carrito.getValueAt(indice, 1).toString();
                String precio = tbl_carrito.getValueAt(indice, 2).toString();

                tabla.addCell(codigo);
                tabla.addCell(nombre);
                tabla.addCell(precio);
                
                
                String dato = codigo + nombre + precio;
                }
                
                
                
                documento.add(tabla);
                
                try{
                    Logica_Orden log_orden = new Logica_Orden();
                    Orden orden = new Orden();
                    
                    orden.setDescripcion(txt_prueba.getText());
                    orden.setEmpleado(txt_prueba.getText());
                    orden.setProveedorn(txt_proveedor.getText());
                    orden.setTotal(parseInt(txt_total.getText()));
                    log_orden.almacenarOrden(orden);
                    JOptionPane.showMessageDialog(rootPane, "Producto Guardado Correctamente!!");
                    
                }catch(Exception err){
                    JOptionPane.showMessageDialog(rootPane, "Error: " + err.getMessage());
                
                }
                
                
        
                

                
                JOptionPane.showMessageDialog(rootPane, "PDF generado correctamente!!");
                
             
                
            }catch(Exception err){
                System.out.println("Error: " + err.getMessage());
            }
            documento.close();
        }catch(Exception err){ 
            System.out.println("Error: " + err.getMessage());
        }
    }//GEN-LAST:event_btn_generar_ordenActionPerformed

    
    
