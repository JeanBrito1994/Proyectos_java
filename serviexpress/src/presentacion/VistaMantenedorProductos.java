    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        Logica_producto log_producto = new Logica_producto();
        try{
            if(tbl_productos.getSelectedRow()==-1){
                JOptionPane.showMessageDialog(rootPane, "Seleccione una opción");
            }else{

                int indice = tbl_productos.getSelectedRow();
                String nombre = tbl_productos.getValueAt(indice, 0).toString();

                log_producto.eliminarProducto(nombre);

                JOptionPane.showMessageDialog(rootPane, "PRODUCTO SE DEHABILITÓ CORRECTAMENTE");
                actualizar_lista();
            }
        }
        catch(Exception err){
            JOptionPane.showMessageDialog(rootPane, "ERROR " + err.getMessage());
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

