    public void actualizar_lista(){
        ArrayList<Producto> listaProductos = new ArrayList<>();
        Logica_Proveedor lg_prov = new Logica_Proveedor();
        VistaMenuProveedor vistaMenuProv = new VistaMenuProveedor();
        try{
            
            listaProductos = lg_prov.listarProductos(vistaMenuProv.prov);
            
            DefaultTableModel tablaModelo = (DefaultTableModel)tbl_productos.getModel();
            tablaModelo.setRowCount(0);
            
            for(Producto x: listaProductos){
               Object[] fila = {
                x.getId(),
                x.getNombre_producto(),
                x.getPrecio()};
               
                tablaModelo.addRow(fila);
                
            }
     
        }
        catch(Exception err){
            
            System.out.println("Error: " + err.getMessage());
        }  
    }
    
