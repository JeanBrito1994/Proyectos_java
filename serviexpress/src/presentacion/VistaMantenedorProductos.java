    private void btn_anadir_productoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_anadir_productoActionPerformed
        Logica_producto log_producto = new Logica_producto();
        Producto producto = new Producto();
        VistaMenuProveedor vistaMenuProv = new VistaMenuProveedor();
        try{

            producto.setNombre_producto(txt_nombre_producto.getText());
            producto.setDescripcion(txta_descripcion.getText());
            producto.setPrecio(parseInt(txt_precio.getText()));

            log_producto.almacenarProducto(producto, vistaMenuProv.prov);

            JOptionPane.showMessageDialog(rootPane, "Producto Guardado Correctamente!!");

            txt_nombre_producto.setText("");
            txta_descripcion.setText("");
            txt_precio.setText("");
            actualizar_lista();

        }
        catch(Exception err){
            JOptionPane.showMessageDialog(rootPane, "Error: " + err.getMessage());
        }
    }//GEN-LAST:event_btn_anadir_productoActionPerformed

